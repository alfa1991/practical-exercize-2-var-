# Задача 1 «Сложная сдача».
# Описание: Одна из задач, в которой лучше исключить человеческий фактор, — подсчёт сдачи.
# Определите, какую сдачу нужно выдать тому, кто купил 4,5кг черешни по цене 34 руб/кг
# Функция для вычисления стоимости черешни
print("Задача 1. Определите, какую сдачу нужно выдать тому, кто купил 4,5кг черешни по цене 34 руб/кг")
def calculate_cherry_cost(weight_kg, price_per_kg):
    return weight_kg * price_per_kg


# Функция для вычисления сдачи
def calculate_change(amount_given, total_cost):
    if amount_given < total_cost:
        return "Недостаточно средств"
    return amount_given - total_cost


# Основная часть программы
if __name__ == "__main__":
    # Параметры задачи
    weight_kg = 4.5
    price_per_kg = 34

    # Вычисление общей стоимости покупки
    total_cost = calculate_cherry_cost(weight_kg, price_per_kg)

    # Ввод суммы от пользователя
    amount_given = float(input("Введите сумму денег, которую вы дали: "))

    # Вычисление сдачи
    change = calculate_change(amount_given, total_cost)

    # Вывод результата
    if isinstance(change, str):
        print(change)
    else:
        print(f"Сдача: {change} руб.")

# Пример использования:
# Введите сумму денег, которую вы дали: 200
# Сдача: 47.0 руб.


